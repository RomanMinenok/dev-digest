/** Minimal line-level diff for the version Diff modal. Computes an LCS over the
   two bodies' lines, then emits add/del/ctx rows. Kept local — the unified-diff
   parser in components/diff-viewer expects patch text, not raw bodies. */

export interface DiffLine {
  kind: "add" | "del" | "ctx";
  text: string;
}

/** Diff `oldBody` → `newBody` at line granularity (LCS backtrace). */
export function diffLines(oldBody: string, newBody: string): DiffLine[] {
  const a = oldBody.split("\n");
  const b = newBody.split("\n");
  const n = a.length;
  const m = b.length;

  // LCS length table.
  const lcs: number[][] = Array.from({ length: n + 1 }, () => new Array<number>(m + 1).fill(0));
  for (let i = n - 1; i >= 0; i--) {
    for (let j = m - 1; j >= 0; j--) {
      lcs[i]![j] = a[i] === b[j] ? lcs[i + 1]![j + 1]! + 1 : Math.max(lcs[i + 1]![j]!, lcs[i]![j + 1]!);
    }
  }

  const out: DiffLine[] = [];
  let i = 0;
  let j = 0;
  while (i < n && j < m) {
    if (a[i] === b[j]) {
      out.push({ kind: "ctx", text: a[i]! });
      i++;
      j++;
    } else if (lcs[i + 1]![j]! >= lcs[i]![j + 1]!) {
      out.push({ kind: "del", text: a[i]! });
      i++;
    } else {
      out.push({ kind: "add", text: b[j]! });
      j++;
    }
  }
  while (i < n) out.push({ kind: "del", text: a[i++]! });
  while (j < m) out.push({ kind: "add", text: b[j++]! });
  return out;
}
